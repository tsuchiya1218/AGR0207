package model;

public class Worker {
	private String w_id;
	private String w_name;
	private String w_pass;

	private String w_day;

	public String getW_id() {
		return w_id;
	}
	public void setW_id(String w_id) {
		this.w_id = w_id;
	}
	public String getW_name() {
		return w_name;
	}
	public void setW_name(String w_name) {
		this.w_name = w_name;
	}
	public String getW_pass() {
		return w_pass;
	}
	public void setW_pass(String w_pass) {
		this.w_pass = w_pass;
	}
	public String getW_day() {
		return w_day;
	}
	public void setW_day(String w_day) {
		this.w_day = w_day;
	}
}
