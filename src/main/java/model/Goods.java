package model;

public class Goods {
	private String g_id;
	private String g_name;
	private String g_imgName;
	private int g_price;
	
	public String getG_id() {
		return g_id;
	}
	public void setG_id(String g_id) {
		this.g_id = g_id;
	}
	public String getG_name() {
		return g_name;
	}
	public void setG_name(String g_name) {
		this.g_name = g_name;
	}
	public String getG_imgName() {
		return g_imgName;
	}
	public void setG_imgName(String g_imgName) {
		this.g_imgName = g_imgName;
	}
	public int getG_price() {
		return g_price;
	}
	public void setG_price(int g_price) {
		this.g_price = g_price;
	}
}
