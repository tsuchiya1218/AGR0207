package model;

public class Store {
	
	private String s_id;
	private String s_area;
	private String s_store;
	private String s_pass;
	
	public String getS_id() {
		return s_id;
	}
	public void setS_id(String s_id) {
		this.s_id = s_id;
	}
	public String getS_area() {
		return s_area;
	}
	public void setS_area(String s_area) {
		this.s_area = s_area;
	}
	public String getS_store() {
		return s_store;
	}
	public void setS_store(String s_store) {
		this.s_store = s_store;
	}
	public String getS_pass() {
		return s_pass;
	}
	public void setS_pass(String s_pass) {
		this.s_pass = s_pass;
	}
	
}
